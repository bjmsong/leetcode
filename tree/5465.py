class Solution:
    def countSubTrees(self, n: int, edges, labels: str):
        """
        - 用edges构造节点指向字典，用以表示有方向的边
        - 从0开始进行深度搜索，当遍历到一个节点时，递归遍历它的所有子节点，然后整合从子节点传上来的字符统计数据，再加上当前节点的字符，就可以得到字符数了。
        """
        from collections import defaultdict
        from collections import Counter

        edge_map = defaultdict(list)
        visited = set([0])
        # 前提条件：每条新的edge只会出现一个新的节点
        for edge in edges:
            if edge[1] not in visited:
                edge_map[edge[0]].append(edge[1])
                visited.add(edge[1])
            if edge[0] not in visited:
                edge_map[edge[1]].append(edge[0])
                visited.add(edge[0])

        def dfs(i):
            # 字符数字典
            data = Counter({labels[i]: 1})
            for nxt in edge_map[i]:
                # 整合子树的字符数
                data += dfs(nxt)
            # 设置当前节点的结果字符数
            ans[i] = data[labels[i]]
            return data

        ans = [1] * n
        dfs(0)
        return ans

